    public void showData() {
        String[] kolom = {"No", "NIM", "Nama", "Kelas", "Jurusan"};
        Object rowData[] = new Object[5];

        try {
            FileInputStream fis = new FileInputStream("mahasiswa");
            ObjectInputStream ois = new ObjectInputStream(fis);
            mhs = (ArrayList) ois.readObject();
            ois.close();
            fis.close();
        } catch (IOException ioe) {
            ioe.printStackTrace();
            return;
        } catch (ClassNotFoundException c) {
            System.out.println("Class not found");
            c.printStackTrace();
            return;
        }

        tableMhs.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < mhs.size(); i++) {
            rowData[0] = i +1;
            rowData[1] = mhs.get(i).getNim();
            rowData[2] = mhs.get(i).getNama();
            rowData[3] = mhs.get(i).getKelas();
            rowData[4] = mhs.get(i).getJurusan();
            ((DefaultTableModel) tableMhs.getModel()).addRow(rowData);
        }
    }
    private void tableMhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMhsMouseClicked
        DefaultTableModel model = (DefaultTableModel) tableMhs.getModel();
        int row = tableMhs.getSelectedRow();
        rowIndex = row;

        textNim.setText(mhs.get(row).getNim());
        textNama.setText(mhs.get(row).getNama());
        textKelas.setText(mhs.get(row).getKelas());
        textJurusan.setText(mhs.get(row).getJurusan());
    }//GEN-LAST:event_tableMhsMouseClicked

    private void btnHapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHapusMouseClicked
        mhs.remove(rowIndex);

        try {
            FileOutputStream fos = new FileOutputStream("mahasiswa");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(mhs);
            oos.close();
            fos.close();
        } catch (IOException ioe) {
            ioe.printStackTrace();
        }
        clearForm();
        showData();
        showMessageDialog(null, "Data berhasil dihapus");
        
    }//GEN-LAST:event_btnHapusMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        mhs.set(rowIndex, new Mahasiswa(textNama.getText().toString(), textNim.getText().toString(), textKelas.getText().toString(), textJurusan.getText().toString()));

        try {
            FileOutputStream fos = new FileOutputStream("mahasiswa");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(mhs);
            oos.close();
            fos.close();
        } catch (IOException ioe) {
            ioe.printStackTrace();
        }
        clearForm();
        showData();
        showMessageDialog(null, "Data berhasil diupdate");
    }//GEN-LAST:event_btnUpdateActionPerformed

