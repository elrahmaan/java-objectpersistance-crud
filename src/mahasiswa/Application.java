    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        mhs.add(new Mahasiswa(textNama.getText().toString(), textNim.getText().toString(), textKelas.getText().toString(), textJurusan.getText().toString()));

        try {
            FileOutputStream fos = new FileOutputStream("mahasiswa");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(mhs);
            oos.close();
            fos.close();
        } catch (IOException ioe) {
            ioe.printStackTrace();
        }
        clearForm();
        showMessageDialog(null, "Data berhasil disimpan");
        
    }//GEN-LAST:event_btnSimpanActionPerformed

